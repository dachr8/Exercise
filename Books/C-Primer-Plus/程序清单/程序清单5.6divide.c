#include <stdio.h>
//divide.c--演示除法
int main(void)
{
    printf("integer division:\t5/4 is %d\n",5/4);
    printf("integer division:\t6/3 is %d\n",6/3);
    printf("integer division:\t7/4 is %d\n",7/4);
    printf("floating division:\t7./4. is %.2f\n",7./4.);
    printf("mixed division:\t\t7./4 is %.2f\n",7./4);

    return 0;
}
